#include <iostream>
using namespace std;

// 2 Напишіть вкладений цикл for, який друкує картинку з нулів 10х10.

void main() 
{
    for (int i = 0; i < 10; ++i) { // вкладений цикл for для друку картинки 10x10 з нулів
        for (int j = 0; j < 10; ++j) 
        {
            cout << "0 "; // друкуємо "0 "
        }
        cout << endl; // перехід на новий рядок після завершення кожного рядка
    }
}